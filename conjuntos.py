#Conjuntos são estruturas de agrupamento
#Não existem elementos repetidos em conjuntos
#Eles não são ordenados
#Conjuntos não permitem fatiamento nem indexação
#Conjuntos também são heterogêneos


conjunto = set()

conjunto.add(10)
conjunto.add(20)
conjunto.add(30)
conjunto.add(10)

print(conjunto)

#Nota-se que ele não acrescenta o elemento 10 novamente


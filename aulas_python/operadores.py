
#Operadores comparação

# a == b -> operador igual comum
# a > b -> maior 
# a < b -> menor
# a <= b -> menor igual
# a >= b -> maior igual
# a != b -> diferente


#Operadores aritméticos

# a + b -> soma comum
# a - b -> subtração comum
# a * b -> multiplicação comum
# a / b -> divisão comum com ponto flutuante
# a // b -> divisão inteira
# a % b -> resto da divisão inteira
# a ** b -> exponencial de base 'a' e expoente 'b'


#Operações lógicas

# and -> 'e' (Quando entrada diferente de binário - caso o ambos valores sejam diferentes de zero
#				o resultado será igual a variável a direita do
#				do operador and. Caso um dos operadores seja 0
#				o resultado sempre será 0)
#			 (Quando entrada binária - Efetua a função 'and' bit a bit)

# not -> negação (tudo que é diferente de zero'é considerado 
#				  True, apenas 0 é condiderado false)

# or  -> 'ou' (Quando entrada diferente de binário - caso o ambos valores sejam diferentes de zero
#				o resultado será igual a variável a esquerda do
#				do operador or. Caso um dos operadores seja 0
#				o resultado sempre será o valor não 0)

#Precedência de operadores lógicos
# not -> and -> or